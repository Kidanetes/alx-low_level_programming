int add(int, int);
int sub(int, int);
int div(int, int);
int mul(int, int);
int mod(int, int);

int add(int num1, int num2)
{
  return (num1 + num2);
}

int sub(int num1, int num2)
{
  return (num1 - num2);
}

int div(int num1, int num2)
{
  return (num1 / num2);
}

int mul(int num1, int num2)
{
  return (num1 * num2);
}

int mod(int num1, int num2)
{
  return (num1 % num2);
}
